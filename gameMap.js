function drawLabyrinth(ctx, cellSize, currentLabyrinthMap) {
  for (let row = 0; row < currentLabyrinthMap.length; row++) {
    for (let col = 0; col < currentLabyrinthMap[row].length; col++) {
      if (currentLabyrinthMap[row][col] === 0) {
        ctx.fillStyle = "black";
      } else if (currentLabyrinthMap[row][col] === 1) {
        ctx.fillStyle = "white";
      } else if (
        currentLabyrinthMap[row][col] === 9 ||
        currentLabyrinthMap[row][col] === 8
      ) {
        ctx.fillStyle = "gold";
      }
      ctx.fillRect(col * cellSize, row * cellSize, cellSize, cellSize);
    }
  }
}

